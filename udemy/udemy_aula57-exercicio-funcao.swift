// 1. Para cada meio litro de suco de laranja você precisa espremer 10 laranjas, 
//então descubra a quantidade de laranjas necessária para fazer 3550 litros de suco.

func fazerSuco(litros : Float){
    var qtdLaranjas : Float = litros * 20 //para .5 litros

    print("Serão necessárias \(qtdLaranjas) laranjas para fazer \(litros)l de suco.")

}

fazerSuco(litros: 3550)
fazerSuco(litros: 10)
fazerSuco(litros: 45967.32)
